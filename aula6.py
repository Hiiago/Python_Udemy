'''
conversão de tipos
type convertion; typecasting; coercion
é o ato de converter um tipo em outro
tipos imutáveis e primitivos:
str, int, float, bool
'''
print(int('1'), type(int('1')))#coerção de uma str para int e mostrando seu tipo
print(int('1') + 1)#coerção de str para int fazendo uma soma
print(float('1') + 1)#coerção de str para float fazendo uma soma
print(bool(' '))
print(str(11) + 'b')
