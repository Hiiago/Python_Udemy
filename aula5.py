#tipo boolean
#ao questionar algo em um programa
#só existem duas respostas:
# True = sim. False = não
#existem varios operadores para 'questionar'
#dentre eles o ==, que é um operador lógico que
#questiona se um valore é igual a outro.
print(10 == 10)#Sim => True(verdadeiro)
print(10 == 11)#Não => False(Falso)
print(type(10 == 10))#bool => (booleano)
print(type(10 == 11))